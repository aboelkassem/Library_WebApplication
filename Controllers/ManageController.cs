        // Post: /Manage/Profile
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit_Profile([Bind(Include = "Id,first_name,last_name,Email,gender,UserName,uploadedUserImage")]user model)
        {
            var CurrentUser = db.users.Where(x => x.UserName == User.Identity.Name).FirstOrDefault();


            ModelState["PasswordHash"].Errors.Clear();  // to remove required Server Side
            if (ModelState.IsValid)
            {
                model.PasswordHash = CurrentUser.PasswordHash;
                model.join_data = CurrentUser.join_data;
                model.PhoneNumber = CurrentUser.PhoneNumber;
                model.EmailConfirmed = CurrentUser.EmailConfirmed;
                model.PhoneNumberConfirmed = CurrentUser.PhoneNumberConfirmed;
                model.IsConfirmed = CurrentUser.IsConfirmed;
                model.Discriminator = CurrentUser.Discriminator;
                model.TwoFactorEnabled = CurrentUser.TwoFactorEnabled;
                model.LockoutEndDateUtc = CurrentUser.LockoutEndDateUtc;
                model.LockoutEnabled = CurrentUser.LockoutEnabled;
                model.AccessFailedCount = CurrentUser.AccessFailedCount;
                model.SecurityStamp = CurrentUser.SecurityStamp;
                // Uplaod Photo =======================================================================================================

                if (model.uploadedUserImage != null)
                {
                    string lastPhoto = db.users.Find(model.Id).photo;
                    if (lastPhoto != "~/Images/default-avatar.png")
                    {
                        System.IO.File.Delete(Request.MapPath(lastPhoto));  // delete From Local Folder
                    }

                    string ImageName = Path.GetFileNameWithoutExtension(model.uploadedUserImage.FileName);           // get name
                    string ImageExtension = Path.GetExtension(model.uploadedUserImage.FileName);                     // get extension
                    ImageName = ImageName + '_' + DateTime.Now.ToString("yymmssfff") + ImageExtension;                // Not To Repeat
                    model.photo = "~/Uploads/Photos/users/" + ImageName;                                         // Save To DB
                    ImageName = Path.Combine(Server.MapPath("~/Uploads/Photos/users/"), ImageName);             // Combine To Local Folder
                    model.uploadedUserImage.SaveAs(ImageName);                                                       // Move it
                }
                else
                {
                    model.photo = db.users.Find(model.Id).photo;     // let the last, don't change
                }
                if (model.Email == null)
                {
                    model.Email = db.users.Find(model.Id).Email;     // let the last, don't change
                }
                if (model.UserName == null)
                {
                    model.UserName = db.users.Find(model.Id).UserName;     // let the last, don't change
                }
                
                var local = db.Set<user>().Local.FirstOrDefault(f => f.Id == model.Id);
                if (local != null)
                {
                    db.Entry(local).State = EntityState.Detached;
                }
                db.Entry(model).State = EntityState.Modified;
                db.SaveChanges();
                TempData["Success"] = "Changed Successfully!";
                return RedirectToAction("Profile");
            }
            return View(model);
        }
